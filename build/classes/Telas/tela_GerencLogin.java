    public void deletar() {
        if(Sessao.isConfirm()){
            Usuario use = new Usuario();
            use.setId(Sessao.getId());
            Delete_Banco del = new Delete_Banco();
            del.Excluir_Usuario(use);
            this.setVisible(false);
            Sessao.setId(0);
            tela_Principal prin = Sessao.getPrin();
            prin.atualizar_Tela();
        }
    }
