package Classes;
public class Nacionalidade {
    public int id;
    public String Pais;

    public Nacionalidade() {
    }

    public Nacionalidade(int id, String Pais) {
        this.id = id;
        this.Pais = Pais;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPais() {
        return Pais;
    }

    public void setPais(String Pais) {
        this.Pais = Pais;
    }
    
    
    
}
