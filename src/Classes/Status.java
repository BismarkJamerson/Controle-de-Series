package Classes;
public class Status {
    public int id;
    public String Status;

    public Status() {
    }

    public Status(int id, String Status) {
        this.id = id;
        this.Status = Status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStatus() {
        return Status;
    }

    public void setStatus(String Status) {
        this.Status = Status;
    }
    
    
    
}
